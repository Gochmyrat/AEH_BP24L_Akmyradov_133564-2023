package pl.pp;
import java.util.Scanner;
public class mySecondApp {
    public static void main(String[] args) {
        int x = 10;

        // double of x is calculated
        int doubleOfX = x * 2;

        // square of x is calculated
        int squaredX = x * x;

        // all three values are printed to the console with explanations
        System.out.println("x = " + x); // Value of x
        System.out.println("Double of x = " + doubleOfX); // double of x
        System.out.println("x squared = " + squaredX); // square of x
    }
}